<h2 class="hcls">Visa</h2>
<p>
Citizens of some countries are required to have a <b>visa</b> for
entry into Finland. Please check if you require a visa from the <a
href="http://formin.finland.fi/public/default.aspx?nodeid=15720&contentlan=2&culture=en-US"
rel="external">Ministry for
				 Foreign Affairs</a>. 
</p><p>				 
				 
				 As part of the
				 visa application process, you may be
				 required to include a <b>visa support
				 letter</b>. To request a visa support letter,
				 <b>register for the conference</b> and then
				 email your request to <a 
				 href="mailto:supportletters@acm.org">supportletters@acm.org</a>,
				 and include
<ul>
<li> Your name</li>
<li> Your mailing address</li>
<li> Your fax number</li>
<li> The name of the conference you are attending (ACM SIGCOMM 2012) and
				 your registration ID </li>
<li> If you are an author of an accepted paper or poster, please
				 provide the title</li>
</ul>

<a href="mailto:supportletters@acm.org" data-role="button" class="button">Request Visa Support Letter</a>
