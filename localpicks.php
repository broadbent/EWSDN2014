<h2 class="hcls">Organizers' Picks</h2>

<p>Here are some of our personal food &amp; drink favorites in Helsinki. But go and explore!</p>

<p>August is still a holiday month in Finland, and restaurants can be closed on some days. It is generally a good idea to call ahead for reservations.</p>

<ul data-role="listview" data-inset="true">
    <li data-role="list-divider">Restaurants</li>
    <li><a href="http://www.virginoil.fi/in-english/info/">
        <h3>Virgin Oil</h3><p>Convenient downtown location, should work for larger groups. Pub and concert venue in the same location.</p></a>
    </li>
    <li><a href="http://www.elite.fi/en">
        <h3>Elite</h3><p>Classic restaurant. Should work for larger groups.</p></a>
    </li>
    <li><a href="http://www.kuurna.fi">
        <h3>Kuurna</h3><p>Small friendly restaurant with a weekly prix fixe menu.</p></a>
    </li>
    <li><a href="http://eat.fi/en/helsinki/patrona">
        <h3>Patrona</h3><p>Authentic Mexican food and imported tequilas. Great service.</p></a>
    </li>
    <li><a href="http://www.asrestaurants.com/EN/restaurants/saari/presentation.html">
        <h3>Saari</h3><p>Island restaurant a few hundred meters off the downtown shore. Nice vistas.</p></a>
    </li>
    <li><a href="http://www.bistrohelsinki.fi">
        <h3>Bistro Helsinki 15</h3><p>Relaxed bistro.</p></a>
    </li>
    <li><a href="http://www.ravintolarafla.fi/index_en.html">
        <h3>Rafla</h3><p>Bar and restaurant with a nice atmosphere and friendly service.</p></a>
    </li>
    <li><a href="http://www.pikkunepal.fi/index.php?l=en">
        <h3>Pikku Nepal</h3><p>Nepalese restaurant with a large menu.</p></a>
    </li>
    <li><a href="http://www.ravintolakuu.info/en/frontpage">
        <h3>Kuu</h3><p>Eclectic restaurant close to several hotels.</p></a>
    </li>
    <li><a href="http://chezdominique.fi/home">
        <h3>Chez Dominique</h3><p><em>The</em> high-end dining experience in Finland. Two Michelin stars. The lunch menu is an affordable option.</p></a>
    </li>
</ul>

